/**
 * Returns the n first items of the specified array
 *
 * @param {array} arr
 * @param {number} n
 *
 * @example
 *    [ 1, 3, 4, 5 ], 2 => [ 1, 3 ]
 *    [ 'a', 'b', 'c', 'd'], 3  => [ 'a', 'b', 'c' ]
 */
function getHead(arr, n) {
  // throw new Error('Not implemented');
  const newArr = [];
  return arr.reduce((accumulator, currentValue, index) => {
    if ((index < n)) {
      accumulator.push(currentValue);
    }
    return accumulator;
  }, newArr);
}


/**
 * Returns the n last items of the specified array
 *
 * @param {array} arr
 * @param {number} n
 *
 * @example
 *    [ 1, 3, 4, 5 ], 2  => [ 4, 5 ]
 *    [ 'a', 'b', 'c', 'd'], 3  => [ 'b', 'c', 'd' ]
 */
function getTail(arr, n) {
  // throw new Error('Not implemented');
  const newArr = [];
  return arr.reduceRight((accumulator, currentValue, index, array) => {
    if ((index > (array.length - 1 - n))) {
      accumulator.unshift(currentValue);
    }
    return accumulator;
  }, newArr);
}


/**
 * Returns CSV represebtation of two-dimentional numeric array.
 * https://en.wikipedia.org/wiki/Comma-separated_values
 *
 * @param {array} arr
 * @return {string}
 *
 * @example
 *    [
 *       [  0, 1, 2, 3, 4 ],
 *       [ 10,11,12,13,14 ],
 *       [ 20,21,22,23,24 ],
 *       [ 30,31,32,33,34 ]
 *    ]
 *           =>
 *     '0,1,2,3,4\n'
 *    +'10,11,12,13,14\n'
 *    +'20,21,22,23,24\n'
 *    +'30,31,32,33,34'
 */
function toCsvText(arr) {
  // throw new Error('Not implemented');
  // return arr.map((value) => (`${value.join(',')}`));
  return arr.reduce((accumulator, currentValue, index, array) => `${accumulator}${currentValue.join(',')}${(index === array.length - 1 ? '' : '\n')}`, '');
}

