/**
 * Sorts digit names
 *
 * @param {array} arr
 * @return {array}
 *
 * @example
 *   [] => []
 *   [ 'nine','one' ]                 => [ 'one', 'nine' ]
 *   [ 'one','two','three' ]          => [ 'one','two', 'three' ]
 *   [ 'nine','eight','nine','eight'] => [ 'eight','eight','nine','nine']
 *   [ 'one','one','one','zero' ]     => [ 'zero','one','one','one' ]
 */
function sortDigitNamesByNumericOrder(arr) {
  // throw new Error('Not implemented');
  function getNumberValue(strValue) {
    let numberValue;
    switch (strValue) {
      case 'zero':
        numberValue = 0;
        break;
      case 'one':
        numberValue = 1;
        break;
      case 'two':
        numberValue = 2;
        break;
      case 'three':
        numberValue = 3;
        break;
      case 'four':
        numberValue = 4;
        break;
      case 'five':
        numberValue = 5;
        break;
      case 'six':
        numberValue = 6;
        break;
      case 'seven':
        numberValue = 7;
        break;
      case 'eight':
        numberValue = 8;
        break;
      case 'nine':
        numberValue = 9;
        break;
      default:
        numberValue = -1;
        break;
    }
    return numberValue;
  }
  return arr.sort((a, b) => getNumberValue(a) - getNumberValue(b));
}

